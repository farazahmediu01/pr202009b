
class Test:

    def plus(self, x, y):
        return x + y

    def minus(self, x, y):
        return x - y

    def mul(self, x, y):
        return x * y

    def div(self, x, y):
        return x / y

a = Test()

print(a.plus(2,2))
print(a.minus(2,2))
print(a.div(2,2))
print(a.mul(2,2))

print(a.plus(8,8))
print(a.minus(8,8))
print(a.div(8,8))
print(a.mul(8,8))


